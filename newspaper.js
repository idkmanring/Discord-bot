async function drawVintageImage(ctx, url, x, y, w, h) {
    try {
        const img = await loadImage(url);
        ctx.save();
        ctx.drawImage(img, x, y, w, h);
        
        const imgData = ctx.getImageData(x, y, w, h);
        const data = imgData.data;
        for (let i = 0; i < data.length; i += 4) {
            const r = data[i], g = data[i+1], b = data[i+2];
            data[i]     = Math.min(255, (r * 0.393) + (g * 0.769) + (b * 0.189)); 
            data[i + 1] = Math.min(255, (r * 0.349) + (g * 0.686) + (b * 0.168)); 
            data[i + 2] = Math.min(255, (r * 0.272) + (g * 0.534) + (b * 0.131)); 
        }
        ctx.putImageData(imgData, x, y);
        
        ctx.strokeStyle = "#3b2b18";
        ctx.lineWidth = 3;
        ctx.strokeRect(x, y, w, h);
        ctx.restore();
    } catch(e) {}
}

function wrapRichTextRTL(ctx, text, box, maxFontSize, defaultColor, highlightColor, align="center") {
    let fontSize = maxFontSize;
    let lineHeight;
    let linesToDraw = [];
    
    let parsedWords = [];
    let isHighlight = false;
    String(text).split(/\s+/).forEach(word => {
        if(!word) return;
        let cleanWord = word;
        let wordHighlight = isHighlight;

        if (cleanWord.startsWith("**") && cleanWord.endsWith("**") && cleanWord.length > 2) {
            wordHighlight = true;
            cleanWord = cleanWord.replace(/\*\*/g, "");
        } else if (cleanWord.startsWith("**")) {
            isHighlight = true;
            wordHighlight = true;
            cleanWord = cleanWord.replace(/\*\*/g, "");
        } else if (cleanWord.endsWith("**")) {
            wordHighlight = true;
            cleanWord = cleanWord.replace(/\*\*/g, "");
            isHighlight = false;
        }
        parsedWords.push({ text: cleanWord, isHighlight: wordHighlight });
    });

    // تصغير الخط تدريجياً حتى يتناسب مع المربع (الحد الأدنى 6 بيكسل)
    while (fontSize > 6) {
        ctx.font = `bold ${fontSize}px Tahoma, sans-serif`;
        lineHeight = fontSize * 1.5;
        const spaceWidth = ctx.measureText(" ").width;

        let currentLine = [];
        let currentLineWidth = 0;
        linesToDraw = [];
        let wordTooWide = false;

        for (let pw of parsedWords) {
            let w = ctx.measureText(pw.text).width;
            if (w > box.w) wordTooWide = true; 

            if (currentLineWidth + w > box.w && currentLine.length > 0) {
                linesToDraw.push({ words: currentLine, width: currentLineWidth - spaceWidth });
                currentLine = [pw];
                currentLineWidth = w + spaceWidth;
            } else {
                currentLine.push(pw);
                currentLineWidth += w + spaceWidth;
            }
        }
        if (currentLine.length > 0) {
            linesToDraw.push({ words: currentLine, width: currentLineWidth - spaceWidth });
        }

        if ((linesToDraw.length * lineHeight) <= box.h && !wordTooWide) {
            break;
        }
        fontSize -= 0.5; 
    }

    // ✂️ مقص الحماية: إذا كان النص ما زال أطول من المربع حتى مع أصغر خط (6px)، نقص الزيادة!
    const maxAllowedLines = Math.floor(box.h / lineHeight);
    if (linesToDraw.length > maxAllowedLines) {
        linesToDraw = linesToDraw.slice(0, maxAllowedLines);
        // إضافة (...) في نهاية آخر سطر ليدل أن النص مقصوص
        if (linesToDraw.length > 0) {
            let lastLineWords = linesToDraw[linesToDraw.length - 1].words;
            if (lastLineWords.length > 0) {
                lastLineWords[lastLineWords.length - 1].text += "...";
            }
        }
    }

    ctx.font = `bold ${fontSize}px Tahoma, sans-serif`;
    ctx.textBaseline = "top";
    ctx.direction = "rtl"; 
    ctx.textAlign = "right";

    // 🛑 توسيط النص عمودياً مع منع خروجه للأعلى (استخدام Math.max لضمان عدم وجود قيمة سالبة)
    let currentY = box.y + Math.max(0, (box.h - (linesToDraw.length * lineHeight)) / 2);
    const spaceWidth = ctx.measureText(" ").width;

    linesToDraw.forEach(lineObj => {
        let startX;
        if (align === "center") startX = box.x + (box.w / 2) + (lineObj.width / 2);
        else if (align === "right") startX = box.x + box.w;
        else startX = box.x + lineObj.width;

        let currentX = startX;
        lineObj.words.forEach(w => {
            ctx.fillStyle = w.isHighlight ? highlightColor : defaultColor;
            ctx.fillText(w.text, currentX, currentY);
            currentX -= (ctx.measureText(w.text).width + spaceWidth);
        });
        currentY += lineHeight;
    });
}

