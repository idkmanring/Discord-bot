function getRandomTemp(arr, vars) {
    if (!arr) return "";
    let temp = arr[Math.floor(Math.random() * arr.length)];
    if (vars) {
        for (const [key, value] of Object.entries(vars)) {
            temp = temp.replace(new RegExp(`{${key}}`, 'g'), String(value));
        }
    }
    return temp;
}

